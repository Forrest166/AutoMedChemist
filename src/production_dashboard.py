def _read_json(path: str | Path) -> dict:
    source = Path(path)
    if not source.exists():
        return {}
    try:
        payload = json.loads(source.read_text(encoding="utf-8")) or {}
    except Exception:
        return {}
    return payload if isinstance(payload, dict) else {}


def _status_level(status: object) -> str:
    value = str(status or "").strip().lower()
    if value in {"pass", "ok", "ready", "ready_with_open_staging", "ready_with_warnings", "ready_with_watch", "healthy", "tracking", "empty", "reviewed", "reviewed_holdout", "partially_approved_holdout", "pending_signoff", "approved", "decision_recorded", "closed_holdout", "compared", "baseline_created", "staged", "review_ready", "dry_run_ready", "ready_with_deferred_source_owner_review", "ready_for_next_feed_drop", "ready_for_promotion", "ready_for_strict_import", "ready_for_sandbox_review", "ready_for_rehearsal", "promoted", "activated", "holdout_ready", "owner_review_recorded", "all_kept_deferred", "closed_no_deferred_conflicts", "awaiting_filled_staging_rows", "awaiting_staged_rows", "awaiting_rows", "awaiting_result_payload", "awaiting_production_results", "awaiting_exact_results", "awaiting_positive_controls", "awaiting_alignment", "awaiting_sources", "review_required", "attention_required", "needs_attention", "blocked_no_active_nonzero_context"}:
        return "pass"
    if value in {"", "missing", "blocked", "blocked_by_validation", "fail", "error", "import_failed", "owner_review_pending"}:
        return "fail"
    return "warn"


def _row(
    gate_id: str,
    label: str,
    status: object,
    details: str,
    *,
    primary: object = "",
    secondary: object = "",
    artifact_path: str | Path = "",
    artifact_csv_path: str | Path = "",
    next_action: str = "",
) -> dict:
    level = _status_level(status)
    return {
        "gate_id": gate_id,
        "label": label,
        "status": status or "missing",
        "level": level,
        "primary": primary,
        "secondary": secondary,
        "details": details,
        "artifact_path": str(artifact_path or ""),
        "artifact_csv_path": str(artifact_csv_path or ""),
        "next_action": next_action,
    }


