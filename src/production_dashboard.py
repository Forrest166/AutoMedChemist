def write_production_dashboard_snapshot(
    report: dict,
    *,
    json_path: str | Path = DEFAULT_PRODUCTION_DASHBOARD_PATH,
    csv_path: str | Path | None = DEFAULT_PRODUCTION_DASHBOARD_CSV_PATH,
) -> None:
    out = Path(json_path)
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(json.dumps(report, indent=2, sort_keys=True), encoding="utf-8")
    if not csv_path:
        return
    rows = [dict(row) for row in report.get("rows") or []]
    fields = ["gate_id", "label", "status", "level", "primary", "secondary", "details", "artifact_path", "artifact_csv_path", "next_action"]
    csv_out = Path(csv_path)
    csv_out.parent.mkdir(parents=True, exist_ok=True)
    with csv_out.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        for row in rows:
            writer.writerow({field: row.get(field, "") for field in fields})
